										<p><strong>Abstract:</strong> The decline in crime rates over the last three decades has enlivened the debate over the preferred methods of combating deviant behavior. Simple regressions of crime on income have documented a strong negative relationship, suggesting that crime is a problem of the poor. However, this research has been unable to adjudicate between the competing theories for this negative relationship. One of the most entrenched debates is over the mediating effects of the state. Do changes in income cause declines in deviant behavior because the government is better able to deter crime with expanded budgets? Or is the causal effect primarily the result of increased opportunity costs? I test these competing theories on county-level crime data from the United States between 1982 and 2007, using trade shocks as a source of exogenous variation in income. I conclude that the majority of the income-crime relationship is explained by the direct effects of opportunity costs. However, there is evidence of a small but significant mediation effect in the form of government expenditures on police.</p>
